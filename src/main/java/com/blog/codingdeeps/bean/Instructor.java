package com.blog.codingdeeps.bean;

public class Instructor {

	private int id;
	private String instructorFirstName;
	private String instructorLastName;
	private double instructorNumber;
	private String instructorDate;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getInstructorFirstName() {
		return instructorFirstName;
	}

	public void setInstructorFirstName(String instructorFirstName) {
		this.instructorFirstName = instructorFirstName;
	}

	public String getInstructorLastName() {
		return instructorLastName;
	}

	public void setInstructorLastName(String instructorLastName) {
		this.instructorLastName = instructorLastName;
	}

	public double getInstructorNumber() {
		return instructorNumber;
	}

	public void setInstructorNumber(double instructorNumber) {
		this.instructorNumber = instructorNumber;
	}

	public String getInstructorDate() {
		return instructorDate;
	}

	public void setInstructorDate(String instructorDate) {
		this.instructorDate = instructorDate;
	}

}
